    private void bequalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bequalActionPerformed
        num2 = Double.parseDouble(t1.getText());
        double ans = 0;

        if(operator=="+")
        {
            ans=num1+num2;
        }
        else if(operator=="/")
        {
            ans=num1/num2;
        }
        else if(operator=="-")
        {
            ans=num1-num2;
        }
        else if(operator=="*")
        {
            ans=num1*num2;
        }
        else if(operator=="%")
        {
            ans=num1%num2;
        }

        t1.setText(""+ans);
        operator = null;
                       
    }//GEN-LAST:event_bequalActionPerformed

